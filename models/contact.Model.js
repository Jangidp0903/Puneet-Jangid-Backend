// Import
import mongoose from "mongoose";

// Create Contact Schema
const ContactSchema = new mongoose.Schema(
  {
    name: {
      type: String,
    },
    email: {
      type: String,
    },
    phone: {
      type: String,
    },
    subject: {
      type: String,
    },
    message: {
      type: String,
      required: true,
    },
    isRead: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

// Contact
const Contact = mongoose.model("Contact", ContactSchema);

export default Contact;
